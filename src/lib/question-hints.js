export function getQuestionHint(mode, questionIndex, facets, lang = 'cs') {
  if (mode === 'pid5' && facets && facets.length > 0) {
    const f = facets[0];
    const h = FACET_HINTS[f];
    return h ? (h[lang] || h.cs) : null;
  }
  if (mode === 'lpfs') {
    const sub = getLpfsSubscale(questionIndex);
    const h = sub ? LPFS_SUBSCALE_HINTS[sub] : null;
    return h ? (h[lang] || h.cs) : null;
  }
  return null;
}

/** Get the localized diagnostic explanation string */
export function getDiagExplanation(id, lang = 'cs') {
  const ex = DIAG_EXPLANATIONS[id];
  if (!ex) return null;
  return typeof ex === 'string' ? ex : (ex[lang] || ex.cs);
}

export function getLpfsSubscale(questionIndex) {
  for (const [sub, indices] of Object.entries(LPFS_SUBSCALES)) {
    if (indices.includes(questionIndex)) return sub;
  }
  return null;
}
